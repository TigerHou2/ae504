function y = terrain(x)

    y = nan(size(x));
    
    c1 = x<0;
    c2 = x>=0 & x<=20;
    c3 = x>20 & x<=30;
    c4 = x>30 & x<=70;
    c5 = x>70 & x<=110;
    c6 = x>110& x<=120;
    c7 = x>120;
    
    y(c1) = 0;
    y(c2) = x(c2).*(20-x(c2));
    y(c3) = 3.*(x(c3)-20).*(30-x(c3));
    y(c4) = 1/3.*(x(c4)-30).*(70-x(c4));
    y(c5) = 0;
    y(c6) = (x(c6)-110).*(120-x(c6));
    y(c7) = 0;
end